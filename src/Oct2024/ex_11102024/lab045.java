package Oct2024.ex_11102024;

public class lab045 {
    public static void main(String[] args) {
        // Logical operators
        // ! - NOT , && - AND, || - OR

        boolean a = true;
        boolean b = true;
        boolean c = a || b;
        System.out.println(c);
        System.out.println(!a);
        System.out.println(!!b);
        System.out.println(!!!b);

        boolean d = a && b;
        System.out.println(d);
    }
}
