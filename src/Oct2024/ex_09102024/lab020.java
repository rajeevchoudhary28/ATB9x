package Oct2024.ex_09102024;

public class lab020 {
    public static void main(String[] args) {
        // primitive data type
        byte b = 100; // 8 bits, 1 Byte
        short s = 128; // 16 bits, 2 Byte
        int i = 1000; // 32 bits, 4 Byte
        long phone_number = 99999999; // 64 bits, 8 Byte
        char grade = 'A';  // A,b,c,d, el - ASCII value - int - assigned // 16 bits, 2 Byte

        float f2 = 3.14f;  //32 bits, 4 Byte
        double d = 118.9876543; //  64 bits, 8 Byte

        // Non primitive data type (user defined)
        String name = "Rajeev";  // string a bunch of characters, Array, class user data type - 64 bits, 8 Byte
    }
}
