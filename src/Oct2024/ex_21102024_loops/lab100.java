package Oct2024.ex_21102024_loops;

public class lab100 {
    public static void main(String[] args) {

        // Break and for loop

        for (int i = 0; i < 50; i++) {
            if (i == 5){
                continue;    // skip below code and move to top
            }
            System.out.println(i);
        }
    }
}

