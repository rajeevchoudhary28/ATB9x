package Oct2024.ex_21102024_loops;

public class lab098 {
    public static void main(String[] args) {
        // Shruti - TO find the even numbers from 1 to 50
        for (int i = 0; i <= 50; i++) {
            if (i % 2 == 0) {
                System.out.println("Even");
            }else{
                System.out.println("Odd");
            }

        }
    }
}
