package Oct2024.ex_21102024_loops;

public class lab085 {
    public static void main(String[] args) {

        // for loop
        // help you to repeat the block of code
        // Initialization -> condition ->updation(Increase / decrease)
        int j = 2;
        for (int i = 0; i <= 10; i++){
            // block of code that we can run multiple times
            // i = 0, 1, 2, 3, 4, 5, 6, 7,....
            System.out.println(j + " X " + i + " = " + j * i);
        }

    }
}
