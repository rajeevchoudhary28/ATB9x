package Oct2024.ex_21102024_loops;

public class lab086 {
    public static void main(String[] args) {

        for (int _1 = 0; _1 < 10; _1++){
            System.out.println(_1);

            // int_1 -> 0 - Initialization
            // _1 <= 10 -> condition
            // _1++ -> updation
        }
        for (int i = 0; i <= 10; ++i){          //pre or post increment it will same
            System.out.println(i);
        }
    }
}
